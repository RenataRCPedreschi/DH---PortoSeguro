package CursoFiap;

public class FiapTeste {

	public static void main(String[] args) {

		int x = 5;
		int y = 10;

		int soma = x + y;
		int subtracao = x - y;
		int multiplicacao = x * y;
		int divisao = x / y;
		int resto = x % y;

		System.out.println("Resultado da adição:      " + soma);
		System.out.println("Resultado da subtração:      " + subtracao);
		System.out.println("Resultado da multiplicação:      " + multiplicacao);
		System.out.println("Resultado da divisão:      " + divisao);
		System.out.println("Resultado da módulo:      " + resto);

		x++;
		System.out.println("Resultado da incremento de x:      " + x);

		y--;
		System.out.println("Resultado do decremento de y:      " + y);

		x += 10;
		System.out.println("Atribuição aditiva de x:      " + x);

		y -= 10;
		System.out.println("Atribuição subtrativa de y:      " + y);

	}
}